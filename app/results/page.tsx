"use client";

import Link from "next/link";
import { useRouter } from "next/navigation";
import { useEffect, useMemo, useState } from "react";
import {
  ArrowLeft,
  ArrowRight,
  CheckCircle2,
  Download,
  ExternalLink,
  FileDown,
  Loader2,
  RefreshCw,
  ShieldCheck,
  Sparkles,
  X
} from "lucide-react";
import { Chrome } from "@/components/complee/Chrome";
import { StepShell } from "@/components/complee/StepShell";
import { useAssessment } from "@/components/complee/assessment-context";
import {
  countryAuthority,
  countryName,
  downloadAnalysisCsv,
  downloadSubmissionPack,
  priorityTone,
  readinessScore,
  statusTone
} from "@/lib/complee-frontend";
import type {
  AnalysisResult,
  EvidenceMatrixItem,
  Priority,
  RequirementStatus,
  RoadmapTask
} from "@/lib/types";

const PAGE_SIZE = 10;

export default function ResultsPage() {
  const router = useRouter();
  const {
    profile,
    selectedServices,
    uploadedDocuments,
    result,
    analysisError,
    agentSeedStatus,
    seedLiveAgent,
    resetAssessment
  } = useAssessment();
  const [selectedItem, setSelectedItem] = useState<EvidenceMatrixItem | null>(null);
  const [page, setPage] = useState(1);
  const score = readinessScore(result);
  const targetAuthority = countryAuthority(profile.targetCountry);

  useEffect(() => {
    if (!result && !analysisError) {
      router.replace("/documents");
    }
  }, [analysisError, result, router]);

  const rows = result?.matrix ?? [];
  const totalPages = Math.max(1, Math.ceil(rows.length / PAGE_SIZE));
  const visibleRows = rows.slice((page - 1) * PAGE_SIZE, page * PAGE_SIZE);
  const roadmapByOwner = useMemo(() => groupRoadmap(result?.roadmap ?? []), [result]);

  if (!result) {
    return (
      <Chrome>
        <StepShell
          eyebrow="Step 4 of 4 - Results dashboard"
          title="No completed assessment yet"
          description={analysisError || "Run the evidence step to generate results."}
        >
          <Link
            href="/documents"
            className="inline-flex min-h-[44px] items-center gap-2 rounded-lg bg-navy px-5 py-3 text-[14px] font-medium text-navy-foreground shadow-sm"
          >
            Back to documents
            <ArrowRight className="h-4 w-4" aria-hidden="true" />
          </Link>
        </StepShell>
      </Chrome>
    );
  }

  return (
    <Chrome>
      <StepShell
        eyebrow="Step 4 of 4 - Results dashboard"
        title={`${profile.companyName} expansion readiness`}
        width="wide"
        description={
          <>
            Assessment against{" "}
            <span className="font-medium text-navy">{targetAuthority}</span>{" "}
            requirements for entering{" "}
            <span className="font-medium text-navy">
              {countryName(profile.targetCountry)}
            </span>
            .
            <br />
            Generated by the existing `/api/analyze` backend.
          </>
        }
        headerAside={<ReadinessBadge score={score} />}
      >
        <div className="grid grid-cols-2 gap-3 sm:gap-4 md:grid-cols-4">
          <StatCard
            label="Total requirements"
            value={result.summary.totalRequirements}
            accent="bg-muted-foreground/40"
          />
          <StatCard label="Covered" value={result.summary.covered} accent="bg-success" />
          <StatCard
            label="Partial"
            value={result.summary.partiallyCovered}
            accent="bg-warn"
          />
          <StatCard
            label="Missing"
            value={result.summary.notEvidenced + result.summary.needsHumanReview}
            accent="bg-danger"
          />
        </div>

        {result.diagnostics ? (
          <DiagnosticsPanel result={result} />
        ) : null}

        <div className="overflow-hidden rounded-2xl border border-border bg-card shadow-sm">
          <div className="flex flex-wrap items-center justify-between gap-3 border-b border-border px-5 py-4 sm:px-6">
            <div>
              <h2 className="text-[15px] font-semibold text-navy">Readiness matrix</h2>
              <p className="mt-1 text-[12px] text-muted-foreground">
                Showing {(page - 1) * PAGE_SIZE + 1}-
                {Math.min(page * PAGE_SIZE, rows.length)} of {rows.length} requirements
              </p>
            </div>
            <div className="flex flex-wrap gap-2">
              <button
                type="button"
                onClick={() => downloadAnalysisCsv(result)}
                className="inline-flex items-center gap-2 rounded-lg border border-border bg-card px-3 py-2 text-[12px] font-medium text-navy transition hover:bg-surface-muted"
              >
                <Download className="h-3.5 w-3.5" aria-hidden="true" />
                CSV
              </button>
              <button
                type="button"
                onClick={() =>
                  downloadSubmissionPack(
                    result,
                    profile,
                    selectedServices,
                    uploadedDocuments
                  )
                }
                className="inline-flex items-center gap-2 rounded-lg bg-navy px-3 py-2 text-[12px] font-medium text-navy-foreground transition hover:bg-navy/90"
              >
                <FileDown className="h-3.5 w-3.5" aria-hidden="true" />
                Submission pack
              </button>
            </div>
          </div>

          <div className="hidden overflow-x-auto sm:block">
            <table className="w-full text-[13px]">
              <thead>
                <tr className="bg-surface-muted text-left text-[11px] uppercase tracking-[0.06em] text-muted-foreground">
                  <th className="px-5 py-3 font-medium">Requirement</th>
                  <th className="px-4 py-3 font-medium">Domain</th>
                  <th className="px-4 py-3 font-medium">Priority</th>
                  <th className="px-4 py-3 font-medium">Status</th>
                  <th className="px-4 py-3 font-medium text-right">Confidence</th>
                  <th className="px-4 py-3 font-medium" />
                </tr>
              </thead>
              <tbody>
                {visibleRows.map((item) => (
                  <tr
                    key={item.requirementId}
                    className="border-t border-border transition-colors hover:bg-surface-muted/60"
                  >
                    <td className="px-5 py-3.5">
                      <div className="font-medium text-navy">{item.requirementTitle}</div>
                      <div className="mt-0.5 text-[11px] text-muted-foreground">
                        {item.regulatoryReference ?? item.requirementId}
                      </div>
                    </td>
                    <td className="px-4 py-3.5 text-muted-foreground">{item.domain}</td>
                    <td className="px-4 py-3.5">
                      <PriorityPill priority={item.priority} />
                    </td>
                    <td className="px-4 py-3.5">
                      <StatusPill status={item.status} />
                    </td>
                    <td className="px-4 py-3.5 text-right tabular-nums text-foreground">
                      {Math.round(item.confidence * 100)}%
                    </td>
                    <td className="px-4 py-3.5 text-right">
                      <button
                        type="button"
                        onClick={() => setSelectedItem(item)}
                        className="whitespace-nowrap text-[12px] font-medium text-brand transition hover:text-brand/80"
                      >
                        Details
                      </button>
                    </td>
                  </tr>
                ))}
              </tbody>
            </table>
          </div>

          <div className="divide-y divide-border sm:hidden">
            {visibleRows.map((item) => (
              <button
                key={item.requirementId}
                type="button"
                onClick={() => setSelectedItem(item)}
                className="w-full px-5 py-4 text-left"
              >
                <div className="flex items-start justify-between gap-3">
                  <div>
                    <p className="text-[14px] font-medium text-navy">
                      {item.requirementTitle}
                    </p>
                    <p className="mt-1 text-[12px] text-muted-foreground">{item.domain}</p>
                  </div>
                  <StatusPill status={item.status} />
                </div>
              </button>
            ))}
          </div>

          {totalPages > 1 ? (
            <div className="flex items-center justify-between border-t border-border px-5 py-3 text-[12px] text-muted-foreground">
              <button
                type="button"
                disabled={page === 1}
                onClick={() => setPage((value) => Math.max(1, value - 1))}
                className="rounded-md px-2 py-1 font-medium text-navy disabled:opacity-40"
              >
                Previous
              </button>
              <span>
                Page {page} of {totalPages}
              </span>
              <button
                type="button"
                disabled={page === totalPages}
                onClick={() => setPage((value) => Math.min(totalPages, value + 1))}
                className="rounded-md px-2 py-1 font-medium text-navy disabled:opacity-40"
              >
                Next
              </button>
            </div>
          ) : null}
        </div>

        <section className="grid gap-5 lg:grid-cols-[0.95fr_1.05fr]">
          <div className="rounded-2xl border border-border bg-card p-5 shadow-sm sm:p-6">
            <div className="mb-4 flex items-center justify-between gap-3">
              <div>
                <h2 className="text-[16px] font-semibold text-navy">Execution roadmap</h2>
                <p className="mt-1 text-[13px] text-muted-foreground">
                  Owned tasks generated for requirements that still need work.
                </p>
              </div>
              <Sparkles className="h-5 w-5 text-brand" aria-hidden="true" />
            </div>
            <div className="space-y-4">
              {Object.entries(roadmapByOwner).map(([owner, tasks]) => (
                <div key={owner} className="rounded-xl border border-border bg-surface-muted p-4">
                  <div className="mb-3 flex items-center justify-between">
                    <h3 className="text-[13px] font-semibold uppercase tracking-[0.08em] text-navy">
                      {owner}
                    </h3>
                    <span className="rounded-full bg-card px-2.5 py-1 text-[11px] text-muted-foreground">
                      {tasks.length} task{tasks.length === 1 ? "" : "s"}
                    </span>
                  </div>
                  <ul className="space-y-3">
                    {tasks.slice(0, 3).map((task) => (
                      <li key={`${owner}-${task.title}`} className="text-[13px]">
                        <p className="font-medium text-navy">{task.title}</p>
                        <p className="mt-1 text-muted-foreground">
                          {task.deadline} - {task.acceptanceCriteria}
                        </p>
                      </li>
                    ))}
                  </ul>
                </div>
              ))}
              {result.roadmap.length === 0 ? (
                <div className="rounded-xl border border-success/30 bg-success-soft p-4 text-[13px] text-success-foreground">
                  No remediation tasks were generated.
                </div>
              ) : null}
            </div>
          </div>

          <div className="rounded-2xl border border-border bg-card p-5 shadow-sm sm:p-6">
            <div className="mb-4 flex items-start gap-3">
              <div className="flex h-10 w-10 shrink-0 items-center justify-center rounded-lg bg-brand-soft text-brand">
                <ShieldCheck className="h-5 w-5" aria-hidden="true" />
              </div>
              <div>
                <h2 className="text-[16px] font-semibold text-navy">Live Agent bridge</h2>
                <p className="mt-1 text-[13px] leading-relaxed text-muted-foreground">
                  Push this profile into the existing Live Agent so regulation changes can
                  become reviewable remediation plans.
                </p>
              </div>
            </div>
            <button
              type="button"
              onClick={() => void seedLiveAgent()}
              disabled={agentSeedStatus.state === "saving"}
              className="inline-flex min-h-[44px] items-center gap-2 rounded-lg bg-brand px-5 py-3 text-[14px] font-medium text-brand-foreground shadow-sm transition hover:bg-brand/90 disabled:opacity-60"
            >
              {agentSeedStatus.state === "saving" ? (
                <Loader2 className="h-4 w-4 animate-spin" aria-hidden="true" />
              ) : (
                <Sparkles className="h-4 w-4" aria-hidden="true" />
              )}
              Seed Live Agent
            </button>
            {agentSeedStatus.message ? (
              <p
                className={`mt-3 text-[13px] ${
                  agentSeedStatus.state === "error"
                    ? "text-danger"
                    : "text-muted-foreground"
                }`}
              >
                {agentSeedStatus.message}
              </p>
            ) : null}
            <div className="mt-5">
              <Link
                href="/agent"
                className="inline-flex items-center gap-2 text-[13px] font-medium text-brand"
              >
                Open Live Agent
                <ArrowRight className="h-3.5 w-3.5" aria-hidden="true" />
              </Link>
            </div>
          </div>
        </section>

        <div className="flex flex-wrap items-center justify-between gap-3">
          <Link
            href="/documents"
            className="inline-flex items-center gap-2 rounded px-1 py-1 text-[13px] text-muted-foreground transition hover:text-foreground"
          >
            <ArrowLeft className="h-3.5 w-3.5" aria-hidden="true" />
            Back to documents
          </Link>
          <button
            type="button"
            onClick={() => {
              resetAssessment();
              router.push("/profile");
            }}
            className="inline-flex min-h-[44px] items-center gap-2 rounded-lg border border-border bg-card px-5 py-3 text-[14px] font-medium text-navy shadow-sm transition hover:bg-surface-muted"
          >
            <RefreshCw className="h-4 w-4" aria-hidden="true" />
            Run another assessment
          </button>
        </div>

        {selectedItem ? (
          <DetailsDrawer item={selectedItem} onClose={() => setSelectedItem(null)} />
        ) : null}
      </StepShell>
    </Chrome>
  );
}

function groupRoadmap(tasks: RoadmapTask[]): Record<string, RoadmapTask[]> {
  return tasks.reduce<Record<string, RoadmapTask[]>>((groups, task) => {
    groups[task.owner] ??= [];
    groups[task.owner].push(task);
    return groups;
  }, {});
}

function ReadinessBadge({ score }: { score: number }) {
  return (
    <div className="rounded-2xl border border-border bg-card p-4 text-center shadow-sm">
      <div className="text-[36px] font-semibold leading-none text-navy">{score}%</div>
      <div className="mt-1 text-[12px] font-medium text-muted-foreground">readiness score</div>
    </div>
  );
}

function StatCard({
  label,
  value,
  accent
}: {
  label: string;
  value: number;
  accent: string;
}) {
  return (
    <div className="rounded-2xl border border-border bg-card p-4 shadow-sm">
      <div className={`mb-3 h-1.5 w-10 rounded-full ${accent}`} />
      <div className="text-[28px] font-semibold text-navy">{value}</div>
      <div className="mt-1 text-[12px] text-muted-foreground">{label}</div>
    </div>
  );
}

function DiagnosticsPanel({ result }: { result: AnalysisResult }) {
  const diagnostics = result.diagnostics;
  if (!diagnostics) return null;

  return (
    <div className="rounded-2xl border border-border bg-card p-5 shadow-sm sm:p-6">
      <div className="mb-4 flex flex-wrap items-center justify-between gap-3">
        <div>
          <h2 className="text-[16px] font-semibold text-navy">Analysis diagnostics</h2>
          <p className="mt-1 text-[13px] text-muted-foreground">
            Engine: {diagnostics.engine}
            {diagnostics.model ? ` - ${diagnostics.model}` : ""} - Persistence:{" "}
            {diagnostics.persistence.status}
          </p>
        </div>
        <div className="inline-flex items-center gap-2 rounded-full bg-success-soft px-3 py-1 text-[12px] font-medium text-success-foreground">
          <CheckCircle2 className="h-3.5 w-3.5" aria-hidden="true" />
          API result loaded
        </div>
      </div>
      {diagnostics.warnings.length > 0 ? (
        <ul className="mb-4 space-y-2 text-[13px] text-muted-foreground">
          {diagnostics.warnings.slice(0, 3).map((warning) => (
            <li key={warning}>- {warning}</li>
          ))}
        </ul>
      ) : null}
      <div className="flex flex-wrap gap-2">
        {diagnostics.regulatorySources.slice(0, 5).map((source) => (
          <a
            key={source.url}
            href={source.url}
            target="_blank"
            rel="noreferrer"
            className="inline-flex items-center gap-1.5 rounded-full border border-border bg-surface-muted px-3 py-1.5 text-[12px] text-muted-foreground transition hover:text-navy"
          >
            {source.label}
            <ExternalLink className="h-3 w-3" aria-hidden="true" />
          </a>
        ))}
      </div>
    </div>
  );
}

function StatusPill({ status }: { status: RequirementStatus }) {
  const tone = statusTone(status);
  const classes =
    tone === "success"
      ? "border-success/25 bg-success-soft text-success-foreground"
      : tone === "warn"
        ? "border-warn/25 bg-warn-soft text-warn-foreground"
        : tone === "danger"
          ? "border-danger/25 bg-danger-soft text-danger"
          : "border-brand/25 bg-brand-soft text-brand";

  return (
    <span className={`inline-flex whitespace-nowrap rounded-full border px-2.5 py-1 text-[12px] font-semibold ${classes}`}>
      {status}
    </span>
  );
}

function PriorityPill({ priority }: { priority: Priority }) {
  const tone = priorityTone(priority);
  const classes =
    tone === "danger"
      ? "border-danger/25 bg-danger-soft text-danger"
      : tone === "warn"
        ? "border-warn/25 bg-warn-soft text-warn-foreground"
        : tone === "brand"
          ? "border-brand/25 bg-brand-soft text-brand"
          : "border-border bg-surface-muted text-muted-foreground";

  return (
    <span className={`inline-flex whitespace-nowrap rounded-full border px-2.5 py-1 text-[12px] font-semibold ${classes}`}>
      {priority}
    </span>
  );
}

function DetailsDrawer({
  item,
  onClose
}: {
  item: EvidenceMatrixItem;
  onClose: () => void;
}) {
  return (
    <div className="fixed inset-0 z-50 flex justify-end bg-navy/35 p-3 sm:p-6">
      <button
        type="button"
        aria-label="Close details"
        className="absolute inset-0 cursor-default"
        onClick={onClose}
      />
      <aside className="relative flex h-full w-full max-w-[560px] flex-col overflow-hidden rounded-2xl border border-border bg-card shadow-2xl">
        <div className="flex items-start justify-between gap-4 border-b border-border p-5">
          <div>
            <p className="text-[12px] font-semibold uppercase tracking-[0.12em] text-muted-foreground">
              Requirement details
            </p>
            <h2 className="mt-1 text-[20px] font-semibold text-navy">
              {item.requirementTitle}
            </h2>
          </div>
          <button
            type="button"
            onClick={onClose}
            className="flex h-8 w-8 items-center justify-center rounded-lg text-muted-foreground hover:bg-surface-muted"
            aria-label="Close"
          >
            <X className="h-4 w-4" aria-hidden="true" />
          </button>
        </div>
        <div className="flex-1 space-y-5 overflow-y-auto p-5">
          <Detail label="Status">
            <StatusPill status={item.status} />
          </Detail>
          <Detail label="Evidence found">{item.evidenceFound}</Detail>
          {item.evidenceExcerpt ? (
            <Detail label="Evidence excerpt">
              <blockquote className="rounded-xl border border-border bg-surface-muted p-3 text-[13px] leading-relaxed text-muted-foreground">
                {item.evidenceExcerpt}
              </blockquote>
            </Detail>
          ) : null}
          <Detail label="Missing evidence">
            {item.missingEvidence.length > 0 ? (
              <ul className="space-y-1">
                {item.missingEvidence.map((evidence) => (
                  <li key={evidence}>- {evidence}</li>
                ))}
              </ul>
            ) : (
              "No missing evidence listed."
            )}
          </Detail>
          <Detail label="Recommended task">{item.recommendedTask}</Detail>
          {item.sourceUrl ? (
            <a
              href={item.sourceUrl}
              target="_blank"
              rel="noreferrer"
              className="inline-flex items-center gap-2 text-[13px] font-medium text-brand"
            >
              Open regulatory source
              <ExternalLink className="h-3.5 w-3.5" aria-hidden="true" />
            </a>
          ) : null}
        </div>
      </aside>
    </div>
  );
}

function Detail({ label, children }: { label: string; children: React.ReactNode }) {
  return (
    <div>
      <p className="mb-1.5 text-[11px] font-semibold uppercase tracking-[0.12em] text-muted-foreground">
        {label}
      </p>
      <div className="text-[13px] leading-relaxed text-foreground">{children}</div>
    </div>
  );
}
